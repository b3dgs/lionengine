package com.b3dgs.lionengine.example.game.raster;

import com.b3dgs.lionengine.LionEngineException;
import com.b3dgs.lionengine.UtilMath;
import com.b3dgs.lionengine.game.feature.FeatureInterface;
import com.b3dgs.lionengine.game.feature.FeatureModel;
import com.b3dgs.lionengine.game.feature.RoutineUpdate;
import com.b3dgs.lionengine.game.feature.Services;
import com.b3dgs.lionengine.game.feature.Setup;
import com.b3dgs.lionengine.game.feature.Transformable;

/**
 * Dino implementation
 */
@FeatureInterface
public final class Dino extends FeatureModel implements RoutineUpdate
{
    private final Transformable transformable;

    private int count;

    /**
     * Create feature.
     * 
     * @param services The services reference (must not be <code>null</code>).
     * @param setup The setup reference (must not be <code>null</code>).
     * @param transformable The transformable feature.
     * @throws LionEngineException If invalid arguments.
     */
    public Dino(Services services, Setup setup, Transformable transformable)
    {
        super(services, setup);

        this.transformable = transformable;

        transformable.setLocationX(120);
    }

    @Override
    public void update(double extrp)
    {
        transformable.setLocationY(UtilMath.sin(count) * 100 + 130);
        count++;
    }
}
